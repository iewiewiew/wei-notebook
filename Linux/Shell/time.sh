# 脚本说明：打印时间.
#-----------------------------------------------------------------------------

time1=`date +%Y-%m-%d`
time2=`date +%Y-%m-%d_%H:%M:%S`
day=`date +%F`

echo -e "无颜色：$time1"
echo -e "红色：\033[31m$time2\033[0m"
echo -e "绿色：\033[32m$day\033[0m"