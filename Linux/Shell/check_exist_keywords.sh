#!bin/bash

#-----------------------------------------------------------------------------
# author: wmh
# 脚本说明：检测启动后输出的日志是否包含关键字, 包含在退出程序, 不包含则继续.
#-----------------------------------------------------------------------------

var=`cd /tmp/bin && su username stop.sh; su username start.sh`

echo "var 关键字: $var"

if [[ $var = *not* ]];then
    echo "包含关键字啦, 退出"
    exit 1
else
    echo "不包含关键字, 正常输出"
fi